function showBranchQuestion(bq) {
  // 复用 quiz card，但换成分支题专属样式
  const card = $('#quiz-card');
  const answersEl = $('#answers');

  // 隐藏进度条和返回按钮
  document.querySelector('.progress-wrap').style.display = 'none';
  document.querySelector('.quiz-nav').style.display = 'none';

  // 添加分支题标识
  $('#question-number').textContent = '最后一题';
  $('#question-number').style.color = 'var(--gold)';

  // hint + 题干
  const hintSpan = document.createElement('span');
  hintSpan.className = 'branch-hint';
  hintSpan.textContent = bq.hint || '';

  $('#question-text').innerHTML = '';
  if (bq.hint) {
    $('#question-text').appendChild(hintSpan);
    $('#question-text').appendChild(document.createElement('br'));
  }
  $('#question-text').appendChild(document.createTextNode(bq.text));

  // 选项
  answersEl.innerHTML = '';
  bq.options.forEach((opt) => {
    const btn = document.createElement('button');
    btn.className = 'btn-answer btn-branch';
    btn.textContent = opt.text;
    btn.addEventListener('click', () => {
      // 标记选中
      answersEl.querySelectorAll('.btn-answer').forEach(b => b.classList.remove('selected'));
      btn.classList.add('selected');
      state.branchVariant = opt.variant;

      setTimeout(() => {
        // 恢复 progress 和 nav 状态（为重新测试做准备）
        document.querySelector('.progress-wrap').style.display = '';
        document.querySelector('.quiz-nav').style.display = '';
        showResult();
      }, 350);
    });
    answersEl.appendChild(btn);
  });

  // 动画进入
  card.style.animation = 'none';
  card.offsetHeight;
  card.style.animation = 'fade-up 0.35s ease forwards';

  showStage('stage-quiz');
}


