function startLoading() {
  showStage('stage-loading');

  const bar      = $('#loading-bar');
  const hintEl   = $('#loading-hint');
  let   progress = 0;
  let   hintIdx  = 0;

  // Rotate hints
  const hintTimer = setInterval(() => {
    hintIdx = (hintIdx + 1) % LOADING_HINTS.length;
    hintEl.textContent = LOADING_HINTS[hintIdx];
  }, 700);

  // Advance bar
  const barTimer = setInterval(() => {
    progress += Math.random() * 12 + 4;
    if (progress >= 100) {
      progress = 100;
      bar.style.width = '100%';
      clearInterval(barTimer);
      clearInterval(hintTimer);

      // Calculate result, then branch or show directly
      setTimeout(() => {
        calculateResult();
        // Only Turbulent types get a branch question
        const bq = !state.assertive
          ? BRANCH_QUESTIONS[state.gender]?.[state.mbti]
          : null;
        if (bq) {
          showBranchQuestion(bq);
        } else {
          state.branchVariant = null;  // Assertive → single character, no variant suffix
          showResult();
        }
      }, 400);
      return;
    }
    bar.style.width = `${progress}%`;
  }, 180);
}


/* ──────────────────────────────────────────
   MBTI CALCULATION
────────────────────────────────────────── */

// 计算单一维度的胜出方、百分比与倾向强度标签
function _dimStat(scoreA, scoreB, letterA, letterB) {
  const total = scoreA + scoreB;
  if (total === 0) return { winner: letterA, loser: letterB, pct: 50, strength: '均衡' };
  const pctA = Math.round((scoreA / total) * 100);
  const winner = pctA >= 50 ? letterA : letterB;
  const loser  = pctA >= 50 ? letterB : letterA;
  const pct    = pctA >= 50 ? pctA : 100 - pctA;
  const strength = pct >= 80 ? '显著' : pct >= 65 ? '明显' : pct >= 55 ? '轻微' : '均衡';
  return { winner, loser, pct, strength };
}

function calculateResult() {
  // ── 1. 累加所有答题分数 ──
  const totals = { E:0, I:0, S:0, N:0, T:0, F:0, J:0, P:0, At:0, Tu:0 };

  QUESTIONS.forEach((q, qi) => {
    const selected = state.answers[qi];
    if (selected === undefined) return;
    const optScores = q.options[selected]?.scores || {};
    Object.keys(optScores).forEach(k => {
      totals[k] = (totals[k] || 0) + optScores[k];
    });
  });

  state.scores = totals;

  // ── 2. 四维度判定（平局偏向第一字母，与 MBTI 惯例一致）──
  const EI = _dimStat(totals.E, totals.I, 'E', 'I');
  const SN = _dimStat(totals.S, totals.N, 'S', 'N');
  const TF = _dimStat(totals.T, totals.F, 'T', 'F');
  const JP = _dimStat(totals.J, totals.P, 'J', 'P');

  state.mbti = [EI.winner, SN.winner, TF.winner, JP.winner].join('');

  // ── 3. Assertive / Turbulent 判定 ──
  // At/Tu 来自 9 道关键题的隐藏维度；平局 → Assertive
  state.assertive = (totals.At >= totals.Tu);
  const AT = _dimStat(totals.At, totals.Tu, 'A', 'T');

  // ── 4. 存储各维度统计，供 trait-bars 等 UI 使用 ──
  state.dimPct = { EI, SN, TF, JP, AT };
}


