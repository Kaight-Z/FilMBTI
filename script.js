function buildFilmStrip() {
  const strip = $('#film-strip');
  strip.innerHTML = '';
  const total = QUESTIONS.length;
  for (let i = 0; i < total; i++) {
    const frame = document.createElement('div');
    frame.className = 'film-frame';
    frame.id = `frame-${i}`;
    strip.appendChild(frame);
  }
}

function updateFilmStrip(currentIndex) {
  const total = QUESTIONS.length;
  for (let i = 0; i < total; i++) {
    const frame = $(`#frame-${i}`);
    if (!frame) continue;
    frame.classList.remove('filled', 'current');
    if (i < currentIndex)      frame.classList.add('filled');
    else if (i === currentIndex) frame.classList.add('current');
  }
  $('#progress-current').textContent = currentIndex + 1;
  $('#progress-total').textContent   = total;
}

function renderQuestion(index) {
  const q = QUESTIONS[index];
  if (!q) return;

  // Card content
  $('#question-number').textContent = `Q${String(index + 1).padStart(2, '0')}`;
  $('#question-text').textContent   = q.text;

  // Animate card in
  const card = $('#quiz-card');
  card.style.animation = 'none';
  card.offsetHeight; // reflow
  card.style.animation = 'fade-up 0.35s ease forwards';

  // Build answer buttons
  const answersEl = $('#answers');
  answersEl.innerHTML = '';

  q.options.forEach((opt, i) => {
    const btn = document.createElement('button');
    btn.className  = 'btn-answer';
    btn.textContent = opt.text;

    // Restore previously selected answer if user went back
    if (state.answers[index] === i) btn.classList.add('selected');

    btn.addEventListener('click', () => selectAnswer(index, i));
    answersEl.appendChild(btn);
  });

  // Back button
  $('#btn-back').disabled = (index === 0);

  updateFilmStrip(index);
}

function selectAnswer(questionIndex, optionIndex) {
  // Visual: mark selected
  $$('.btn-answer').forEach((b, i) => {
    b.classList.toggle('selected', i === optionIndex);
  });

  // Save answer
  state.answers[questionIndex] = optionIndex;
  ANALYTICS.onAnswer(questionIndex, optionIndex);  // ← 统计作答 + 防抖写 Supabase

  // Advance after short delay
  setTimeout(() => {
    if (questionIndex < QUESTIONS.length - 1) {
      state.currentQ = questionIndex + 1;
      renderQuestion(state.currentQ);
    } else {
      // All questions answered → loading
      startLoading();
    }
  }, 300);
}

$('#btn-back').addEventListener('click', () => {
  if (state.currentQ > 0) {
    state.currentQ--;
    renderQuestion(state.currentQ);
  }
});

function startQuiz() {
  // Reset quiz state
  state.answers       = [];
  state.currentQ      = 0;
  state.scores        = { E:0,I:0,S:0,N:0,T:0,F:0,J:0,P:0,At:0,Tu:0 };
  state.mbti          = null;
  state.assertive     = null;
  state.branchVariant = null;
  state.dimPct        = null;

  buildFilmStrip();
  renderQuestion(0);
  showStage('stage-quiz');
}


